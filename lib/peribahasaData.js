// Helper untuk dapatkan data mengikut tahap (Silibus CEKAP & CITA)
export const getPeribahasaByLevel = (level) => {
  switch (level) {
    case 'P3':
      return PERIBAHASA_P3; // CITA Syllabus
    case 'P4':
      return [...PERIBAHASA_P3, ...PERIBAHASA_P4]; // CEKAP Syllabus: All P3 + P4
    case 'P5':
    case 'P6':
      return [...PERIBAHASA_P3, ...PERIBAHASA_P4, ...PERIBAHASA_P5_P6]; // CEKAP Syllabus: ALL
    default:
      return PERIBAHASA_P3;
  }
};